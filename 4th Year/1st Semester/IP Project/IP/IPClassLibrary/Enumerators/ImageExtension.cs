/// <summary>
/// Image Extension, used in Opening & Saving Images
/// </summary>
public enum ImageExtension
{
    /// <summary>
    /// PPM extension, used for our ImageTypes
    /// </summary>
    PPM,

    /// <summary>
    /// BMP, Bitmap supported extension
    /// </summary>
    BMP,

    /// <summary>
    /// JPG, Bitmap supported extension
    /// </summary>
    JPG,

    /// <summary>
    /// JPEG, Bitmap supported extension
    /// </summary>
    JPEG,

    /// <summary>
    /// GIF, Bitmap supported extension
    /// </summary>
    GIF,

    /// <summary>
    /// PNG, Bitmap supported extension
    /// </summary>
    PNG,

    /// <summary>
    /// ICON, Bitmap supported extension
    /// </summary>
    ICO,
}